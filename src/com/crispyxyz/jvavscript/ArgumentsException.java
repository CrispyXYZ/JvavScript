package com.crispyxyz.jvavscript;

@SuppressWarnings("serial")
public class ArgumentsException extends RuntimeException {
	public ArgumentsException() {}

	public ArgumentsException(java.lang.String message) {
		super(message);
	}

	public ArgumentsException(java.lang.String message, java.lang.Throwable cause) {
		super(message, cause);
	}

	public ArgumentsException(java.lang.Throwable cause) {
		super(cause);
	}
}
